export function getCarsByPath(path: string): Car[] {
  return mockCars.filter(car => car.path === path);
}

export function groupCarsByModel(cars: Car[]) {
  const grouped: { [key: string]: Car[] } = {};
  cars.forEach(car => {
    const key = `${car.year}-${car.make}-${car.model}`;
    if (!grouped[key]) grouped[key] = [];
    grouped[key].push(car);
  });
  return grouped;
}
